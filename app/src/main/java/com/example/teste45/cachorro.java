package com.example.teste45;

public class cachorro {
    private int id;
    private String nome;
    private String raca;
    private int idadeHumana;
    private int idadeCachorro;

    // Getters e Setters

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getRaca() {
        return raca;
    }

    public void setRaca(String raca) {
        this.raca = raca;
    }

    public int getIdadeCachorro() {
        return idadeCachorro;
    }

    public void setIdadeCachorro(int idadeCachorro) {
        this.idadeCachorro = idadeCachorro;
    }

    public int getIdadeHumana() {
        return idadeHumana;
    }

    public void setIdadeHumana(int idadeHumana) {
        this.idadeHumana = idadeHumana;
    }
}
